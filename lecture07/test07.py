# การเข้าถึงทุกข้อมูลใน List และ Tuple
#        0   1     2       3         4          5
var1 = [10, 20, 'Hello', True, (111, 'wow'), 'สมชาย']

var2 = (10, 20, 'Hello', True, (111, 'wow'), 'สมชาย')

for data in var1 : # data คือตัวแปร
    print(data)

for data in var2 : # data คือตัวแปร
    print(data)